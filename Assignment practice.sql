--7. Bulk Order Summary For each order, find the total number of distinct products ordered.
--Display the Order ID and the Product Count, but only for orders that contain more than 3
--different products.
SELECT 
    so.order_id,
    COUNT(DISTINCT soi.product_id) AS product_count
FROM sales.orders so
JOIN sales.order_items soi
    ON so.order_id = soi.order_id
GROUP BY so.order_id
HAVING COUNT(DISTINCT soi.product_id) > 3;


