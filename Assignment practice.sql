--Discount Impact Analysis

--Analyze the revenue lost due to discounts across different product categories.
--For each product category, calculate:
--The total potential revenue (assuming no discounts were applied).
--The actual revenue (after applying the discount).
--The total discount amount (the total revenue lost due to discounts).

--Use GROUP BY to aggregate the data by category, and HAVING to display only those categories where the total discount amount exceeds $5,000.



SELECT 
    pc.category_name,
    SUM(soi.quantity * soi.list_price) AS total_potential_revenue,  -- Potential revenue (no discount)
    SUM(soi.quantity * soi.list_price * (1 - soi.discount)) AS actual_revenue,  -- Actual revenue (after discount)
    SUM(soi.quantity * soi.list_price *soi.discount) AS total_discount_amount     -- Total discount amount (revenue lost)
FROM sales.order_items soi
JOIN production.products pp
    ON soi.product_id = pp.product_id
JOIN production.categories pc
    ON pp.category_id = pc.category_id
GROUP BY pc.category_name
HAVING SUM(soi.quantity * soi.list_price * soi.discount) > 5000
ORDER BY 
    total_discount_amount DESC;

